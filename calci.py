#!/usr/bin/python3

# Addition function
def add(num1,num2):
    return num1+num2

# Subtract function
def sub(num1,num2):
    return num1-num2

# Multiplication function
def mul(num1,num2):
    return num1*num2

# Division function
def div(num1,num2):
    return num1/num2

# call add function
res_add = add(52,15)
print(res_add)

# call sub function
res_sub = sub(52,15)
print(res_sub)

# call mul function
res_mul = mul(52,10)
print(res_mul)

# call div function
res_div = div(52,15)
print(res_div)
